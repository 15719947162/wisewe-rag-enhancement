"""文档清洗提示词预设模块

本模块提供针对不同学科类型和文档排版的清洗提示词预设，用于指导 LLM 清洗文档内容。

核心概念：
-----------
1. **学科预设（SUBJECT_PRESETS）**：
   根据文档的学科类型选择相应的提示词，每种预设针对特定领域的 OCR 错误模式
   和特殊术语进行优化，确保关键信息不被误删。

2. **排版预设（LAYOUT_PRESETS）**：
   根据文档的物理排版结构选择相应的提示词，帮助 LLM 理解文本块之间的
   逻辑关系，避免因排版导致的语义断裂问题。

3. **组合使用**：
   通过 build_system_prompt() 函数将学科预设和排版预设组合成完整的系统提示词。
   例如："医学教材" + "双列排版" → 医学双栏文档的最佳清洗效果。

主要出口：
---------
- SUBJECT_PRESETS: 学科类型预设字典
- LAYOUT_PRESETS: 排版类型预设字典
- build_system_prompt(): 组合生成完整提示词

使用场景：
---------
1. CLI 模式：用户通过命令行参数指定学科和排版类型
2. Web 前端：用户在交互界面选择预设（下拉菜单或自动检测）
3. 自动检测：未来可根据文档内容特征自动选择预设（待开发）

示例：
------
>>> # 获取医学教材双列排版的提示词
>>> prompt = build_system_prompt("医学教材", "双列排版")
>>> # 输出：基础清洗规则 + 医学专业术语说明 + 双列排版处理说明
"""
from __future__ import annotations

# ---------------------------------------------------------------------------
# Subject-type presets
# ---------------------------------------------------------------------------

# ---------------------------------------------------------------------------
# 基础提示词构建块
# ---------------------------------------------------------------------------
# 以下常量定义了提示词的基础组成部分：
# - _BASE_PROMPT: 所有清洗任务的通用基础指令
# - *_SUFFIX: 针对特定学科的补充指令，追加到基础提示词后

# 基础清洗提示词
# ---------------
# 这是所有清洗任务的通用框架，包含：
# 1. 角色定义：文档清洗助手
# 2. 核心任务：OCR 修正 + 价值判断
# 3. 输出格式：JSON 数组，每个元素包含 index、action、fixed_text、reason
# 4. 处理标准：丢弃标准（广告、版权等）+ 保留标准（正文、表格、标题）
#
# 使用场景：
# - 适用于通用文档类型
# - 作为所有学科预设的基础框架
_BASE_PROMPT = (
    "你是文档清洗助手。对给定的文本块列表，完成：\n"
    "1. 修正明显的 OCR 错误（错别字、乱码）\n"
    "2. 判断每个文本块的信息价值\n\n"
    "对每个文本块返回 JSON 对象：\n"
    '{"index": 序号, "action": "keep"|"discard"|"fix", '
    '"fixed_text": "修正后文本（仅fix时）", "reason": "简短说明"}\n\n'
    "丢弃标准：纯广告、版权声明、无实质信息的装饰文本、乱码无法修复\n"
    "保留标准：有知识价值的正文、表格数据、标题\n\n"
    "返回一个 JSON 数组，包含所有文本块的处理结果。"
)

# 医学教材专项补充提示词
# ----------------------
# 针对医学文献的 OCR 特点和信息保护需求设计：
#
# 主要保护内容：
# - 解剖术语（如"股骨"、"主动脉"等）
# - 药品名称（化学名、商品名）
# - 剂量单位（mg、mmol/L、mmHg 等，这些极易被 OCR 误识别）
# - 临床数据（检验指标、病理描述）
# - 用药信息（剂量、途径、疗程）
#
# 典型 OCR 错误示例：
# - mg → rng（易混淆）
# - mmHg → mmllg（下标丢失）
# - μg → ug（希腊字母 μ 被替换）
#
# 使用场景：
# - 医学教材、临床指南、药品说明书
# - 医学论文、病历文档
_MEDICAL_SUFFIX = (
    "\n\n【医学教材专项说明】\n"
    "- 保留解剖术语、药品名称、剂量单位（如 mg、mmol/L、mmHg）\n"
    "- 修正常见医学 OCR 错误，例如：mg 误识为 rng、mmHg 误识为 mmllg、ug 误识为 ug\n"
    "- 保留病理描述、临床数据、检验指标及正常参考范围\n"
    "- 保留药物剂量、给药途径、疗程等临床信息"
)

# 理工教材专项补充提示词
# ----------------------
# 针对理工科文献的 OCR 特点和信息保护需求设计：
#
# 主要保护内容：
# - 公式符号（希腊字母、数学符号）
# - 变量名（alpha、beta、delta、sigma 等）
# - 数学单位（m、kg、s、A 等 SI 单位）
# - 图表引用（图1-2、表3-1、式(2.5) 等）
# - 实验数据（测量值、误差范围、置信区间）
#
# 典型 OCR 错误示例：
# - 0 ↔ O（数字零与字母 O 混淆）
# - 1 ↔ l（数字一与字母小写 L 混淆）
# - 希腊字母被误识别为拉丁字母（如 β → B）
#
# 使用场景：
# - 数学、物理、化学教材
# - 工程技术文档、实验报告
# - 计算机科学论文（含公式和算法描述）
_STEM_SUFFIX = (
    "\n\n【理工教材专项说明】\n"
    "- 保留公式符号、数学单位、变量名（如 alpha、beta、delta、sigma）\n"
    "- 修正数字 OCR 错误，例如：0 与 O 混淆、1 与 l 混淆\n"
    "- 保留图表引用编号（如图1-2、表3-1、式(2.5)）\n"
    "- 保留实验数据、测量单位、误差范围等定量信息"
)

# 文史教材专项补充提示词
# ----------------------
# 针对文史类文献的 OCR 特点和信息保护需求设计：
#
# 主要保护内容：
# - 古文引用（文言文不应被当作乱码删除）
# - 人名、地名（历史人物、地理名称）
# - 年代信息（朝代、公元纪年、历史事件名称）
# - 引用文献（出处标注、脚注内容）
#
# 文史类 OCR 特殊问题：
# - 繁简混用：古籍影印版可能包含繁体字，需要统一处理
# - 文言文：古汉语表达方式不应被误判为乱码
# - 引用格式：脚注、尾注、参考文献格式多样
#
# 使用场景：
# - 历史教材、文学史、哲学著作
# - 古籍整理、文献学研究
# - 社会科学类教材
_HUMANITIES_SUFFIX = (
    "\n\n【文史教材专项说明】\n"
    "- 保留古文引用和注释，不将文言文视为乱码\n"
    "- 修正繁简混用问题，统一为简体中文（除非原文明确使用繁体）\n"
    "- 保留人名、地名、年代信息（如朝代、公元纪年、历史事件名称）\n"
    "- 保留引用文献的出处标注和脚注内容"
)

# ---------------------------------------------------------------------------
# 学科类型预设字典
# ---------------------------------------------------------------------------
# SUBJECT_PRESETS 提供针对不同学科类型的完整清洗提示词。
#
# 字典结构：
# -----------
# Key（str）: 预设名称，用于用户选择和显示（如 "医学教材"、"理工教材"）
# Value（str）: 完整的清洗提示词，由 _BASE_PROMPT + 学科专属 _SUFFIX 组成
#
# 预设列表：
# ---------
# - "通用（默认）": 适用于普通文档、技术文档、新闻文章等
# - "医学教材": 针对医学文献优化，保护专业术语和临床数据
# - "理工教材": 针对理工科文献优化，保护公式符号和实验数据
# - "文史教材": 针对文史类文献优化，保护古文引用和历史信息
#
# 扩展方法：
# ---------
# 如需添加新的学科预设：
# 1. 定义新的 _SUFFIX 常量（如 _LEGAL_SUFFIX 法律文档）
# 2. 在 SUBJECT_PRESETS 中添加条目：{"法律文档": _BASE_PROMPT + _LEGAL_SUFFIX}
# 3. 确保预设名称与前端下拉菜单选项对应
#
# 使用示例：
# ---------
# >>> medical_prompt = SUBJECT_PRESETS["医学教材"]
# >>> # 包含基础规则 + 医学专业说明
#
# >>> generic_prompt = SUBJECT_PRESETS.get("未知类型", SUBJECT_PRESETS["通用（默认）"])
# >>> # 使用 get() 方法提供默认值，避免 KeyError
SUBJECT_PRESETS: dict[str, str] = {
    "通用（默认）": _BASE_PROMPT,
    "医学教材": _BASE_PROMPT + _MEDICAL_SUFFIX,
    "理工教材": _BASE_PROMPT + _STEM_SUFFIX,
    "文史教材": _BASE_PROMPT + _HUMANITIES_SUFFIX,
}

# ---------------------------------------------------------------------------
# 排版类型预设字典
# ---------------------------------------------------------------------------
# LAYOUT_PRESETS 提供针对不同文档排版结构的补充说明。
#
# 字典结构：
# -----------
# Key（str）: 预设名称，用于用户选择和显示
# Value（str）: 排版说明文本，将被追加到学科预设之后
#             空字符串表示无需特殊排版说明（如单列排版）
#
# 预设列表：
# ---------
# - "单列（默认）": 空字符串，无特殊说明，适用于大多数普通文档
# - "双列排版": 针对双栏杂志、学术期刊等，提醒 LLM 注意左右列的逻辑关系
# - "图文混排": 针对包含大量图片的教材，提醒保留图注、图题等说明文字
#
# 为什么需要排版预设？
# ------------------
# PDF 解析器按阅读顺序提取文本块，但复杂排版会打乱这个顺序：
#
# 1. **双列排版问题**：
#    - PDF 可能先提取左列上半部分，再提取右列上半部分
#    - 导致同一段落被拆分成两个不相关的文本块
#    - LLM 需要理解"相邻块可能是左右列关系，而非上下文关系"
#
# 2. **图文混排问题**：
#    - 图片说明文字可能被单独提取为独立文本块
#    - 这些说明性文字容易被误判为"短文本"而删除
#    - LLM 需要识别并保留图注、图题等信息
#
# 扩展方法：
# ---------
# 如需添加新的排版预设：
# 1. 在 LAYOUT_PRESETS 中添加条目：{"三列排版": "【排版说明】文档为三列排版..."}
# 2. 注意排版说明应以"【排版说明】"开头，保持格式一致
#
# 使用示例：
# ---------
# >>> # 双列排版说明
# >>> layout_hint = LAYOUT_PRESETS["双列排版"]
# >>> print(layout_hint)
# >>> # 输出：【排版说明】文档为双列排版...
#
# >>> # 单列排版（无特殊说明）
# >>> layout_hint = LAYOUT_PRESETS.get("单列（默认）", "")
# >>> # 返回空字符串
LAYOUT_PRESETS: dict[str, str] = {
    "单列（默认）": "",
    "双列排版": (
        "【排版说明】文档为双列排版，相邻文本块可能是同一段落的左右两列，"
        "请结合上下文判断文本块之间的逻辑关系，避免将跨列断句误判为独立段落。"
    ),
    "图文混排": (
        "【排版说明】文档含大量图片和文字混排，图片说明文字可能被单独提取为文本块，"
        "请结合上下文判断信息价值，图注、图题等说明性文字应予以保留。"
    ),
}

# ---------------------------------------------------------------------------
# 提示词构建器
# ---------------------------------------------------------------------------


def build_system_prompt(subject_key: str, layout_key: str) -> str:
    """组合学科预设和排版预设，生成完整的系统提示词。

    这是本模块的主要出口函数，将学科类型的清洗规则与排版结构的说明
    组合成一个完整的系统提示词，供 LLM 清洗文档时使用。

    工作原理：
    ----------
    1. 从 SUBJECT_PRESETS 中获取学科预设（如"医学教材"）
    2. 从 LAYOUT_PRESETS 中获取排版说明（如"双列排版"）
    3. 如果排版说明非空，则追加到学科预设之后
    4. 返回组合后的完整提示词

    参数说明：
    ----------
    subject_key : str
        学科预设的键名，必须是 SUBJECT_PRESETS 中的有效键。
        常用值："通用（默认）"、"医学教材"、"理工教材"、"文史教材"
        如果传入无效键，将回退到"通用（默认）"

    layout_key : str
        排版预设的键名，必须是 LAYOUT_PRESETS 中的有效键。
        常用值："单列（默认）"、"双列排版"、"图文混排"
        如果传入无效键，将使用空字符串（无排版说明）

    返回值：
    --------
    str
        完整的系统提示词，包含：
        - 基础清洗规则（_BASE_PROMPT）
        - 学科专项说明（如果有）
        - 排版结构说明（如果有）

    使用示例：
    ----------
    >>> # 示例 1: 医学教材 + 双列排版
    >>> prompt = build_system_prompt("医学教材", "双列排版")
    >>> # 输出包含三部分：
    >>> # 1. 基础清洗规则
    >>> # 2. 医学专业术语保护说明
    >>> # 3. 双列排版处理说明
    >>> print(prompt[:100])  # 查看提示词开头
    >>> # "你是文档清洗助手。对给定的文本块列表..."

    >>> # 示例 2: 通用文档 + 单列排版
    >>> prompt = build_system_prompt("通用（默认）", "单列（默认）")
    >>> # 只包含基础清洗规则，无学科和排版补充

    >>> # 示例 3: 理工教材 + 图文混排
    >>> prompt = build_system_prompt("理工教材", "图文混排")
    >>> # 包含基础规则 + 理科专业说明 + 图文混排说明

    >>> # 示例 4: 处理无效输入
    >>> prompt = build_system_prompt("未知学科", "未知排版")
    >>> # 自动回退到"通用（默认）"预设，排版说明为空
    >>> # 不会抛出异常，保证函数健壮性

    在 CLI 中的使用：
    ----------------
    # backend/cli.py
    from core.cleaner.prompts import build_system_prompt

    # 用户通过参数指定学科和排版
    subject = args.subject  # 如 "医学教材"
    layout = args.layout    # 如 "双列排版"

    # 构建提示词
    system_prompt = build_system_prompt(subject, layout)

    # 传递给 LLM 清洗器
    cleaner = LLMCleaner(system_prompt=system_prompt)
    clean_result = cleaner.clean(blocks)

    注意事项：
    ----------
    1. 函数不会验证预设键的有效性，无效键会自动回退到默认值
    2. 排版说明非空时会用 "\\n\\n" 与学科预设分隔，保持可读性
    3. 返回的提示词是静态字符串，不包含动态内容（如文件名、页码等）
    """
    # 获取学科预设，如果键不存在则使用默认预设
    base = SUBJECT_PRESETS.get(subject_key, SUBJECT_PRESETS["通用（默认）"])

    # 获取排版说明，如果键不存在则使用空字符串
    layout_hint = LAYOUT_PRESETS.get(layout_key, "")

    # 如果排版说明非空，追加到学科预设之后（用空行分隔）
    if layout_hint:
        return base + "\n\n" + layout_hint

    # 否则直接返回学科预设
    return base
