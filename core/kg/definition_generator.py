"""
实体定义生成器模块
===================

【知识图谱是什么？】
想象一下，你在读一本厚厚的教科书。书中提到了很多概念，比如"机器学习"、"神经网络"等。
如果让你整理这些概念，你可能会做一个笔记，写下每个概念的名字和它的解释。
知识图谱就是这样的"笔记系统"——它把文档中的关键信息提取出来，形成一张知识网络。

【这个模块的作用】
在知识图谱中，每个"实体"（比如一个概念、一个人名、一个设备）都需要一个"定义"。
就像字典一样，你得知道每个词是什么意思。

这个模块的工作就是：
从原始文档中，为每个实体找到它的"定义"——就是第一句话或包含该实体名称的句子。
这样用户在查看知识图谱时，不仅能看到实体名字，还能看到它是什么意思。

【工作流程】
1. 拿到一个实体（比如"机器学习"）
2. 找到这个实体来自哪些文档片段（chunks）
3. 在这些片段中，找到第一个包含实体名字的句子
4. 把这个句子作为实体的"定义"返回

【举例】
假设文档说："机器学习是人工智能的一个分支，它使计算机能够从数据中学习。"
那么"机器学习"这个实体的定义就是这句话的前120个字符。
"""

from __future__ import annotations

from core.models.content_block import Chunk
from core.models.entity import Entity


def generate_definition(entity: Entity, chunk_map: dict[str, Chunk]) -> str:
    """
    为实体生成定义（从原文中摘取描述性句子）

    这个函数就像是一个"找定义专家"——你告诉它一个实体，它去原文里找这个实体的解释。

    【参数说明】
    entity: 需要生成定义的实体对象
        - 包含实体名称（entity.name）
        - 包含该实体来自哪些文档片段的ID列表（entity.source_chunks）

    chunk_map: 文档片段的字典
        - key 是片段ID
        - value 是片段内容
        - 用来根据ID快速找到对应的原文

    【返回值】
    返回找到的定义句子，如果没有找到，就返回实体名称本身

    【工作原理 - 用大白话说】
    1. 遍历这个实体来源的所有文档片段
    2. 在每个片段中，尝试提取一个"定义性句子"
    3. 一旦找到，就返回这个句子
    4. 如果所有片段都找不到，就返回实体名字

    【为什么这样设计？】
    通常，作者在文档中第一次提到某个概念时，会给出定义。
    所以我们在原文中找包含实体名字的句子，很可能就是它的定义。
    """
    # 遍历实体来源的所有文档片段
    for chunk_id in entity.source_chunks:
        # 从字典中获取对应的文档片段
        chunk = chunk_map.get(chunk_id)
        if not chunk:
            # 如果片段不存在，跳过，继续找下一个
            continue

        # 尝试从这个片段中提取定义句子
        snippet = _extract_definition_snippet(entity.name, chunk.content)
        if snippet:
            # 找到了！返回这个定义
            return snippet

    # 所有片段都没找到定义，就返回实体名称
    return entity.name


def _extract_definition_snippet(name: str, content: str) -> str:
    """
    从一段文本中提取实体的定义句子（内部辅助函数）

    这个函数是一个"句子提取器"——从一大段话中，找到第一句话作为候选定义。

    【参数说明】
    name: 实体名称（比如"机器学习"）
    content: 一段文本内容（可能是几百字的段落）

    【返回值】
    返回提取到的定义句子，最多120个字符

    【工作原理 - 用大白话说】
    1. 清理文本（去掉多余空格）
    2. 找到第一句话（以句号、分号或换行分隔）
    3. 如果实体名字在这句话里，直接返回这句话（最多120字）
    4. 如果实体名字不在，就把实体名字加在前面，格式为"实体名：第一句话"

    【为什么找第一句话？】
    在学术和技术文档中，第一句话往往是总结性的、定义性的句子。
    比如："机器学习是人工智能的一个分支。"这就是第一句话，也是定义。

    【为什么限制长度？】
    定义不应该太长，120个字符足够表达一个简洁的定义。
    太长的话，用户看起来就累赘了。
    """
    # 清理文本：去掉首尾空格，把多个连续空格合并成一个
    text = " ".join(content.strip().split())
    if not text:
        return ""

    # 找第一句话：按中文句号、分号、换行符切分，取第一部分
    sentence = text.split("。", 1)[0].split("；", 1)[0].split("\n", 1)[0].strip()
    if not sentence:
        return ""

    # 如果实体名字在句子中，直接返回（最多120字）
    if name in sentence:
        return sentence[:120]

    # 实体名字不在句子中，构造"实体名：句子"的格式
    return f"{name}：{sentence[:100]}"
